package com.examen;

public class PedidoValores {
	private int costeTipo;
	private int costeTam;
	private int costeCod;
	private int costeDom;
	private int costeTotal;

	public int getCosteTotal() {
		return costeTotal;
	}

	public void setCosteTotal(int costeTotal) {
		this.costeTotal = costeTotal;
	}

	public int getCosteDom() {
		return costeDom;
	}

	public void setCosteDom(int costeDom) {
		this.costeDom = costeDom;
	}

	public int getCosteTipo() {
		return costeTipo;
	}

	public void setCosteTipo(int costeTipo) {
		this.costeTipo = costeTipo;
	}

	public int getCosteTam() {
		return costeTam;
	}

	public void setCosteTam(int costeTam) {
		this.costeTam = costeTam;
	}

	public int getCosteCod() {
		return costeCod;
	}

	public void setCosteCod(int costeCod) {
		this.costeCod = costeCod;
	}
}
